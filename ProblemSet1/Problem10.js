/*
Write a program using two for loops to produce the following pattern of asterisks.
Print only a single asterisk to the screen at a time.
Input
None
Output
*****
*****
*****
*****
*****
*/

let tLine = 5
let tChar = 5
for(let i=0;i<tLine;i++){
    let star = ""
    for(let j=0;j<tChar;j++){
        star = star + "*"
    }
    
}
console.log(star)
